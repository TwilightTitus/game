package de.titus.game.core.math.doublepoint;

/**
 * The Class Edge.
 */
public class Edge {

	/** The a. */
	public final Vector a;

	/** The b. */
	public final Vector b;

	/**
	 * Instantiates a new edge.
	 *
	 * @param a the a
	 * @param b the b
	 */
	public Edge(final Vector aA, final Vector aB) {
		super();
		this.a = aA;
		this.b = aB;
	}
}
