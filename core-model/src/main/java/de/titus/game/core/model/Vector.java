package de.titus.game.core.model;

/**
 * The Class Vector.
 */
public class Vector {

	/** The x. */
	public final long	x;

	/** The y. */
	public final long	y;

	/**
	 * Instantiates a new vector.
	 *
	 * @param aX the a X
	 * @param aY the a Y
	 */
	public Vector(final long aX, final long aY) {
		super();
		this.x = aX;
		this.y = aY;
	}

}
